#ifndef WATCHDOG_H
#define WATCHDOG_H
/*#define _POSIX_C_SOURCE 200809L*/

/*#include <sys/types.h>*/


typedef struct watchdog watchdog_t;



/*********************************************************************************************************************************************************************************
 *                                                                 WatchdogStart
********************************************************************************************************************************************************************************
DESCRIPTION
	start the watchdog program.
ARGUMENTS
	user_program_path - path to program to protect.
RETURN VALUE
	in success - pointer to watchdog.
	in failure - NULL.
complexity - o(1). 
********************************************************************************************************************************************************************************/       
watchdog_t *WatchdogStart(char *user_program_path);




/*********************************************************************************************************************************************************************************
 *                                                                 WatchdogStop
********************************************************************************************************************************************************************************
DESCRIPTION
	stop the watchdog program.
ARGUMENTS
	watchdog - pointer to watchdog.
RETURN VALUE
	none.
complexity - o(1). 
********************************************************************************************************************************************************************************/
void WatchdogStop(watchdog_t *watchdog);


#endif /*WATCHDOG_H*/